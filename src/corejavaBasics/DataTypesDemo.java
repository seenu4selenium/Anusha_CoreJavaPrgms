package corejavaBasics;

public class DataTypesDemo {

	public static void main(String[] args) {
		// User will store numbers into a variable.
		byte b = 27;
		short c = 345;
		int a = 968979;
		long d = 1874632210;

		// User will store number with decimal values into a variable.
		float f = 56.123459999f;
		double g = 56.123456789;

		System.out.println(f);
		System.out.println(g);

		// Want to store TRUE or FALSE, will use boolean
		boolean anusha = true;
		boolean i = false;

		// User will store single Number/Alphabet/SpecialCharacter into a variable.
		// char data type you can declare into single quotes
		char abc = '4';
		// char abc ='p';
		// char abc ='&';

		// char z = 'p';
//		------------------
//		char is data type
//		z is variable
//		= is operator
//		'p' is value of z
	}

}
