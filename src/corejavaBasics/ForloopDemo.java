package corejavaBasics;

public class ForloopDemo {
	public static void main(String[] args) {

//		for loop
		for (int i=1; i <= 10; i++) {
			System.out.println(i);
		}

		// While loop
		int i = 1;
		while (i <= 10) {
			System.out.println(i);
			i++;
		}

		// Do-While loop
		int j =123;
		do {
			System.out.println(j);
			j++;
		}while(j <= 10);
		

	}

}
